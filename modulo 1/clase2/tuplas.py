""" 
Una turpla, es basicamente MUY parecida a lo que es una lista, pero sin embargo, son mas seguras.
Basicamente, son inmutables

La sintaxis es casi igual, pero a diferencia de las listas es que usan parentesis
ejemplo: ejemploTurpla = ("manzana", "uva", "banana")

Ventajas:
1- Son inmutables, por ende se utilizan cada vez que queremos datos seguros
2- Son mas rapidas de analizar y de recorrer que las listas.
"""

